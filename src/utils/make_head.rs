pub fn make_head() -> &'static str {
    let head = r#"
    <!DOCTYPE html>
    <html lang="en">
    <head>
    
    <meta charset="utf-8">
    <meta http-equiv="X-UA-Compatible" content="IE=edge,chrome=1" />
    <meta name="viewport" content="width=device-width, initial-scale=1.0">
    
    <title>value</title>
    </head>
    "#;
    return head;
}
